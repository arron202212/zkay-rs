use rccell::RcCell;

//  * Pk derivation part of jsnark's ECDHKeyExchangeGadget

#[derive(Debug, Clone)]
pub struct ZkayEcPkDerivationGadget {
    // The Affine point representation is used as it saves one gate per bit
    pub basePoint: AffinePoint, // The Base point both parties agree to

    pub secretBits: Vec<Option<WireType>>, // the bits of the secret generated by this party
    // (follows little-endian order)

    // gadget output
    pub outputPublicValue: Option<WireType>, // the x-coordinate of the key exchange
    // material to be sent to the other party
    // outputPublicValue = ((this party's
    // secret)*Base).x
    pub outputs: Vec<Option<WireType>>,
}

impl ZkayEcPkDerivationGadget {
    pub fn new(
        secretKey: WireType,
        validateSecret: bool,
        desc: &Option<String>,
        generator: RcCell<CircuitGenerator>,
    ) -> Gadget<ZkayEcGadget<Self>> {
        let mut _self = ZkayEcGadget::<Self>::new(
            desc,
            Self {
                secretBits: secretKey
                    .get_bit_wiresi(Gadget::<ZkayEcGadget<Self>>::SECRET_BITWIDTH as u64, &None)
                    .as_array()
                    .clone(),
                basePoint: AffinePoint::new(
                    Some(CircuitGenerator::create_constant_wirei(
                        generator.clone(),
                        4,
                        &None,
                    )),
                    None,
                ),
                outputPublicValue: None,
                outputs: vec![],
            },
            generator,
        );

        // Hardcode base point
        if validateSecret {
            Gadget::<ZkayEcGadget<Self>>::checkSecretBits(&_self.generator, &_self.t.t.secretBits);
        }

        _self.computeYCoordinates(); // For efficiency reasons, we rely on affine

        // coordinates
        _self.build_circuit();

        _self
    }
}
impl Gadget<ZkayEcGadget<ZkayEcPkDerivationGadget>> {
    fn build_circuit(&mut self) {
        let baseTable = Gadget::<ZkayEcGadget<ZkayEcPkDerivationGadget>>::preprocess(
            &self.t.t.basePoint,
            self.generator.clone(),
        );

        let outputPublicValue = Gadget::<ZkayEcGadget<ZkayEcPkDerivationGadget>>::mul(
            &self.t.t.basePoint,
            &self.t.t.secretBits,
            &baseTable,
            self.generator.clone(),
        )
        .x
        .clone();

        self.t.t.outputs = vec![outputPublicValue.clone()];
        self.t.t.outputPublicValue = outputPublicValue;
    }

    fn computeYCoordinates(&mut self) {
        let x = self
            .t
            .t
            .basePoint
            .x
            .as_ref()
            .unwrap()
            .try_as_constant_ref()
            .unwrap()
            .get_constant();
        self.t.t.basePoint.y = Some(CircuitGenerator::create_constant_wire(
            self.generator.clone(),
            &Gadget::<ZkayEcGadget<ZkayEcPkDerivationGadget>>::computeYCoordinate(x),
            &None,
        ));
    }
}
impl GadgetConfig for Gadget<ZkayEcGadget<ZkayEcPkDerivationGadget>> {
    fn get_output_wires(&self) -> &Vec<Option<WireType>> {
        &self.t.t.outputs
    }
}
