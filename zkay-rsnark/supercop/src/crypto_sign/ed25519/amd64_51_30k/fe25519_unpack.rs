use crate::crypto_sign::ed25519::amd64_51_30k::fe25519::fe25519;
pub fn fe25519_unpack(r: &mut fe25519, x: &[u8; 32]) {
    r.v[0] = x[0] as u64;
    r.v[0] += (x[1] as u64) << 8;
    r.v[0] += (x[2] as u64) << 16;
    r.v[0] += (x[3] as u64) << 24;
    r.v[0] += (x[4] as u64) << 32;
    r.v[0] += (x[5] as u64) << 40;
    r.v[0] += ((x[6] as u64) & 7) << 48;

    r.v[1] = x[6] as u64 >> 3;
    r.v[1] += (x[7] as u64) << 5;
    r.v[1] += (x[8] as u64) << 13;
    r.v[1] += (x[9] as u64) << 21;
    r.v[1] += (x[10] as u64) << 29;
    r.v[1] += (x[11] as u64) << 37;
    r.v[1] += ((x[12] as u64) & 63) << 45;

    r.v[2] = x[12] as u64 >> 6;
    r.v[2] += (x[13] as u64) << 2;
    r.v[2] += (x[14] as u64) << 10;
    r.v[2] += (x[15] as u64) << 18;
    r.v[2] += (x[16] as u64) << 26;
    r.v[2] += (x[17] as u64) << 34;
    r.v[2] += (x[18] as u64) << 42;
    r.v[2] += ((x[19] as u64) & 1) << 50;

    r.v[3] = x[19] as u64 >> 1;
    r.v[3] += (x[20] as u64) << 7;
    r.v[3] += (x[21] as u64) << 15;
    r.v[3] += (x[22] as u64) << 23;
    r.v[3] += (x[23] as u64) << 31;
    r.v[3] += (x[24] as u64) << 39;
    r.v[3] += ((x[25] as u64) & 15) << 47;

    r.v[4] = x[25] as u64 >> 4;
    r.v[4] += (x[26] as u64) << 4;
    r.v[4] += (x[27] as u64) << 12;
    r.v[4] += (x[28] as u64) << 20;
    r.v[4] += (x[29] as u64) << 28;
    r.v[4] += (x[30] as u64) << 36;
    r.v[4] += ((x[31] as u64) & 127) << 44;
}

